
mood = str(input("What mood are you in [happy, nervous, sad, excited, relaxed]: "))

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up maaaaate.")
elif mood == "excited":
    print("Go for it!")
elif mood == "relaxed":
    print("chiiiiill mate.")
else:
    print("I don't recognize this mood.")
